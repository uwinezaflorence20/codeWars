function getPlanetName(id){
  var name;
  switch(id){
    case 1:
      name = 'Mercury'
      return name;
    case 2:
      name = 'Venus'
      return name;
    case 3:
      name = 'Earth'
      return name;
    case 4:
      name = 'Mars'
      return name;
    case 5:
      name = 'Jupiter'
      return name;
    case 6:
      name = 'Saturn'
      return name;
    case 7:
      name = 'Uranus'
      return name;
    case 8:
      name = 'Neptune'
      return name;
  default:
  return name;
      }
}